    public void showDetails(){
        txtMaSV.setText(tblSinhVien.getValueAt(current, 0).toString());
        txtHoTen.setText(tblSinhVien.getValueAt(current, 1).toString());
        txtEmail.setText(tblSinhVien.getValueAt(current, 2).toString());
        txtSDT.setText(tblSinhVien.getValueAt(current, 3).toString());
        Boolean gioitinh = Boolean.parseBoolean(tblSinhVien.getValueAt(current, 4).toString());
        if(gioitinh == true){
            rdoNam.setSelected(true);
        }else{
            rdoNu.setSelected(true);
        }
        txtDiaChi.setText(tblSinhVien.getValueAt(current, 5).toString());
        UpdateHinh(tblSinhVien.getValueAt(current, 6).toString());
    }
    
