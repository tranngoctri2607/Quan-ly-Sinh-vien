    public  void load_data(){
        try {
            Connection con = DriverManager.getConnection(url, user, passWord);
            PreparedStatement ps = con.prepareStatement("select * from Students");
            ResultSet rs = ps.executeQuery();
            tblModel.setRowCount(0);
            Vector vector = null;
            while(rs.next()){
                vector = new Vector();
                vector.add(rs.getString("MaSV")); 
                vector.add(rs.getString("HoTen")); 
                vector.add(rs.getString("Email")); 
                vector.add(rs.getString("SoDt")); 
                vector.add(rs.getBoolean("GioiTinh")); 
                vector.add(rs.getString("DiaChi")); 
                vector.add(rs.getString("Hinh")); 
                tblModel.addRow(vector);
            }
            tblSinhVien.setModel(tblModel);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
